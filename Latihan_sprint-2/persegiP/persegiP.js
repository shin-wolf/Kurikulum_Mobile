function persegiPanjang(p1, l1) {
  // variabel
  p = 4;
  l = 5;

  // hasil
  hasil = p1 * l1;

  // return untuk mengembalikan hasil dari fungction
  return hasil;
}
persegiPanjang(4, 5);
